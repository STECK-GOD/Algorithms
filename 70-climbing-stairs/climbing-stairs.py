class Solution:
    def climbStairs(self, n: int) -> int:
        # Если ступенек 1 или 2, ответ равен n
        if n <= 2:
            return n
        # Создаем список для хранения результатов подзадач
        # dp[i] — количество способов подняться на i-ю ступеньку
        dp = [0] * (n + 1)
        # Базовые случаи
        dp[1] = 1 
        dp[2] = 2
        # Заполняем таблицу итеративно
        for i in range(3, n + 1):
            # Количество способов попасть на текущую ступеньку — 
            # это сумма способов попасть на неё с предыдущей и через одну.
            dp[i] = dp[i - 1] + dp[i - 2]
        # Возвращаем результат для n-й ступеньки
        return dp[n]