import random

# --- ФУНКЦИИ ---
def partition_lomuto(arr, low, high):
    """
    Процедура разделения массива по схеме Ломуто.
    Опорным элементом (pivot) становится последний элемент подмассива.
    """
    pivot = arr[high] # Фиксируем опорный элемент
    i = low - 1       # Указатель на границу элементов, меньших опорного

    # Проходим по всем элементам от начала до предпоследнего
    for j in range(low, high):
        # Если текущий элемент меньше или равен опорному, отправляем его в левую часть
        if arr[j] <= pivot:
            i += 1    # Расширяем границу меньших элементов
            # Меняем местами текущий элемент и элемент на границе
            arr[i], arr[j] = arr[j], arr[i]

    # Устанавливаем опорный элемент сразу после всех элементов, которые меньше его
    arr[i + 1], arr[high] = arr[high], arr[i + 1]
    
    # Возвращаем итоговый индекс опорного элемента
    return i + 1

def randomized_partition(arr, low, high):
    """
    Рандомизация опорного элемента для защиты от худшего случая на отсортированных данных.
    """
    # Выбираем случайный индекс в текущем диапазоне [low, high]
    rand_idx = random.randint(low, high)
    
    # Меняем случайный элемент с последним, чтобы использовать стандартную схему Ломуто
    arr[rand_idx], arr[high] = arr[high], arr[rand_idx]
    
    # Вызываем обычное разделение Ломуто
    return partition_lomuto(arr, low, high)

def quick_sort(arr, low, high):
    """
    Основная рекурсивная функция быстрой сортировки.
    Модифицирует исходный массив (in-place).
    """
    if low < high:
        # pi — индекс опорного элемента, который уже стоит на своем окончательном месте
        pi = randomized_partition(arr, low, high)

        # Рекурсивно сортируем левую часть (элементы меньше опорного)
        quick_sort(arr, low, pi - 1)
        # Рекурсивно сортируем правую часть (элементы больше опорного)
        quick_sort(arr, pi + 1, high)

