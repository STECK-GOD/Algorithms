class Solution:
    def coinChange(self, coins: List[int], amount: int) -> int:
        # Создаем таблицу и заполняем её "бесконечностью" (числом больше суммы)
        # Это нужно, чтобы функция min() работала корректно
        dp = [amount + 1] * (amount + 1)
        # Базовый случай: для суммы 0 нужно 0 монет
        dp[0] = 0
        # Проходим по всем суммам от 1 до нужной нам
        for i in range(1, amount + 1):
            # Пробуем каждую монету из кошелька
            for coin in coins:
                # Если номинал монеты не больше текущей суммы i
                if i - coin >= 0:
                    # Обновляем dp[i], выбирая самый короткий путь
                    dp[i] = min(dp[i], dp[i - coin] + 1)      
        # Если значение в ячейке не изменилось, значит сумму собрать невозможно
        return dp[amount] if dp[amount] != amount + 1 else -1